#include "libft.h"

void	ft_putnbr(int n)
{
	char	c;
	long	nl;

	nl = n;
	if (nl < 0)
	{
		ft_putchar('-');
		nl = -nl;
	}
	if (nl / 10 != 0)
		ft_putnbr(nl / 10);
	c = '0' + (nl % 10);
	ft_putchar(c);
}
