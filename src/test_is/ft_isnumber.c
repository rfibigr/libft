#include "libft.h"

int		ft_isnumber(char *str)
{
	int i;

	i = 0;
	if (!str)
		return (0);
	while (str[i])
	{
		if (!ft_isdigit(str[i]) && (str[0] != '-' || str[0] != '+'))
			return (0);
		i++;
	}
	return (1);
}
